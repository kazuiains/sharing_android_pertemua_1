package com.adiyusuf.pertemuan1.pojo;

public class Mobil {
    private String namaMobil;

    private String warnaMobil;

    private String tahunMobil;

    private int hargaSewa;

    private int batasWaktu;

    public String getNamaMobil() {
        return namaMobil;
    }

    public void setNamaMobil(String namaMobil) {
        this.namaMobil = namaMobil;
    }

    public String getWarnaMobil() {
        return warnaMobil;
    }

    public void setWarnaMobil(String warnaMobil) {
        this.warnaMobil = warnaMobil;
    }

    public String getTahunMobil() {
        return tahunMobil;
    }

    public void setTahunMobil(String tahunMobil) {
        this.tahunMobil = tahunMobil;
    }

    public int getHargaSewa() {
        return hargaSewa;
    }

    public void setHargaSewa(int hargaSewa) {
        this.hargaSewa = hargaSewa;
    }

    public int getBatasWaktu() {
        return batasWaktu;
    }

    public void setBatasWaktu(int batasWaktu) {
        this.batasWaktu = batasWaktu;
    }
}
